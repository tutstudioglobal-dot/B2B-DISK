export function t(key, lang = 'ar') {
  const keys = key.split('.')
  let val = i18n
  for (const k of keys) {
    if (val?.[k] === undefined) return key
    val = val[k]
  }
  if (val?.[lang]) return val[lang]
  if (Array.isArray(val)) return val
  return key
}

export function tArr(arr, lang = 'ar') {
  if (!Array.isArray(arr)) return []
  return arr.map(item => item?.[lang] || item)
}

